"""
Hallucination taxonomy (Maynez et al. 2020, causal reading) for
elaborate-rewriting chains.

Two cascaded LLM-as-a-judge checks on each NOT_SUPPORTED atomic claim:

  Check 1 (CAUSAL: origin of the claim against E_0):
      - intrinsic   → claim is anchored in E_0 and the rewrite has
                      transformed / specialised / distorted source info.
                      Error originates from a BAD TRANSFORMATION.
      - extrinsic   → claim is generated autonomously by the model;
                      its information has its ORIGIN OUTSIDE the source.
                      Pass to Check 2 for world-truth.

  Check 2 (real-world truth, on 'extrinsic' claims only):
      - world_true       → EXTRINSIC_TRUE (parametric memory leak — fact is
                          true in the world but not grounded in E_0)
      - world_false      → EXTRINSIC_FALSE (genuine hallucination — claim
                          is false in the world)
      - world_uncertain  → judge does not have the world knowledge to
                          decide; reported separately

Final taxonomy:
  intrinsic | extrinsic_true | extrinsic_false | extrinsic_uncertain

Design notes:
- Source-supported check is already done by OpenFactScore (label column).
- We use the CAUSAL Maynez reading (transformation vs autonomous generation),
  not the purely logical NLI reading (contradicts vs neutral). The causal
  framing better captures bad transformations that don't produce direct
  logical contradictions (e.g. "eldest son" → "firstborn legitimate heir").
- Check 2 is intentionally conservative: gpt-4o-mini is poor at
  fact-checking obscure entities. We expose 'world_uncertain' so we can
  report a lower- and upper-bound on extrinsic_false, instead of a single
  unreliable point estimate.

References:
- Maynez et al. 2020 (ACL) — On Faithfulness and Factuality in Abstractive
  Summarization. Source of the intrinsic/extrinsic taxonomy.
- Huang et al. 2024 (ACM TOIS) — A Survey on Hallucination in LLMs.
- Wei et al. 2024 (NeurIPS) — SAFE, for the world-truth check rationale.

Usage
-----
  export OPENAI_API_KEY=...

  # Smoke test on 10 claims
  python scripts/15q/hallucination_taxonomy_eval.py --limit 10 \\
         --out /tmp/smoke_halluc.csv

  # Full run on elaborate, 15q
  python scripts/15q/hallucination_taxonomy_eval.py
"""

from __future__ import annotations

import argparse
import json
import os
import sys
import time
from pathlib import Path

import pandas as pd
from openai import OpenAI

REPO_ROOT = Path(__file__).resolve().parent.parent.parent

DEFAULT_DETAILS = REPO_ROOT / "results/15q/rewriting_chains_15q_openfactscore_details.csv"
DEFAULT_CHAINS  = REPO_ROOT / "results/15q/rewriting_chains_15q.csv"
DEFAULT_OUT     = REPO_ROOT / "results/15q/elaborate_hallucination_taxonomy.csv"


# ---------------------------------------------------------------------------
# Check 1 (causal) — what is the ORIGIN of the claim?
# ---------------------------------------------------------------------------
# Maynez 2020, causal reading:
#   - intrinsic = rewrite USES info from the source and TRANSFORMS / DISTORTS
#                 / SPECIALISES it; the claim is "anchored" to E_0
#   - extrinsic = rewrite GENERATES the info AUTONOMOUSLY; no anchor in E_0
#
# Labels: intrinsic | extrinsic
# This is the causal complement to the logical NLI framing
# (contradicts vs neutral).

CHECK1_PROMPT = """\
You are an annotator analysing the ORIGIN of a claim in a rewritten text.

The CLAIM was generated by a language model rewriting a SOURCE passage.
The claim is already known to be NOT directly supported by the source
(OFS flagged it as not-supported).

Decide: is the claim ANCHORED in the source (the rewrite is transforming
information that was in E_0), or is the claim GENERATED AUTONOMOUSLY by
the model (the rewrite is adding information that was NOT in E_0)?

Labels:

  - intrinsic: The claim is anchored in the source. The source mentions
              the SAME ENTITY (person, place, event, object) the claim is
              about, and the rewrite is transforming / specialising /
              distorting / extending information already present.
              The error originates from a BAD TRANSFORMATION of the source.

  - extrinsic: The claim is generated autonomously by the model. The
              source does not mention the entity / topic the claim is
              about, or mentions only the entity but the claim adds
              new information unrelated to anything in the source.
              The information has its ORIGIN OUTSIDE the source.

DECISION RULES

1. ANCHORING IS ABOUT THE ENTITY, NOT THE FACT.
   If the source talks about entity X (even briefly) and the claim is
   about entity X, label `intrinsic` — the rewrite has taken X from the
   source and added/changed something about it.

2. THE SOURCE'S MENTION CAN BE SHORT.
   Even one sentence about the entity in the source is enough to count
   as anchoring. The claim doesn't need to be a verbatim transformation.

3. EXAMPLES OF INTRINSIC (transformation of source content):
   - Source: "Clara Morris (born 1849)"; claim: "Clara Morris was born in 1949"
     → numerical distortion of source info → `intrinsic`.
   - Source: "five children, all by Isabella"; claim: "John fathered three children"
     → quantitative distortion → `intrinsic`.
   - Source: "Walter Calverley was the eldest son and heir of William Calverley"
     claim: "Walter Calverley was the firstborn legitimate heir of Sir William
     Calverley" → adds "legitimate" + "Sir" not in source, but anchored
     → `intrinsic`.
   - Source: "Clive A. Smith co-founded Nelvana with Hirsh and Loubert"
     claim: "He met with Michael Hirsh and Patrick Loubert" → reframes the
     same event → `intrinsic`.
   - Source mentions "Walter Calverley" but not his political role;
     claim: "He played a significant role in the region's politics"
     → extends source entity with extra info → `intrinsic`.

4. EXAMPLES OF EXTRINSIC (generated autonomously, source-unanchored):
   - Source talks about Hyundai Universe luxury coach.
     Claim: "Apple Safari is a major web browser" → unrelated topic
     → `extrinsic`.
   - Source mentions a person briefly but the claim is about a different
     person not in the source → `extrinsic`.
   - Source describes a chemical company; claim about "John Wedgwood's
     horticulture career" (Wedgwood is NOT in source) → `extrinsic`.

5. WHEN THE CLAIM IS ABOUT AN ENTITY NEVER MENTIONED IN THE SOURCE
   → `extrinsic`.

6. WHEN UNCERTAIN, ASK: "did the model take this entity from the source and
   add/change something about it?". If yes → `intrinsic`. If the model
   pulled the entity from elsewhere → `extrinsic`.

7. DO NOT REASON ABOUT REAL-WORLD TRUTH. Focus only on whether the source
   text contains the anchoring entity, and whether the rewrite is
   transforming or generating.

Output ONLY a JSON object: {{"label": "intrinsic"}} or {{"label": "extrinsic"}}.
No reasoning.

SOURCE:
{source}

CLAIM: {claim}
"""


# ---------------------------------------------------------------------------
# Check 2 prompt — is the claim true in the real world?
# ---------------------------------------------------------------------------
# Conservative: default to true; only label false on confident knowledge;
# expose uncertain so we can compute a bound.

CHECK2_PROMPT = """\
You are a careful real-world fact-checker.

Below is a CLAIM. Decide whether the claim is TRUE in the real world,
based ONLY on your own knowledge. You do not have web access.

Labels:
  - world_true       The claim is consistent with established real-world
                    facts that you know.
  - world_false      You ACTIVELY KNOW the claim contradicts established
                    real-world facts. (Strong evidence required.)
  - world_uncertain  You do not have reliable knowledge about the
                    entity / event / number in this claim. You cannot
                    verify it either way.

DECISION RULES (read carefully)
- Plausibility is NOT enough to label `world_true`. But you should label
  `world_true` if the claim aligns with mainstream knowledge.
- For `world_false` you must have specific, confident knowledge that
  contradicts the claim. "It seems wrong" is not enough.
- For an obscure person, place, local statistic, or event that you do
  not personally recognize, label `world_uncertain`. Do NOT guess.
- Specifically:
    * "Person X was born in YYYY" where you do not know X → world_uncertain
    * Local geographic figures (acreage of a small forest, population of
      a small village) where you have no specific knowledge → world_uncertain
    * Mainstream historical/scientific/cultural facts you know well →
      world_true or world_false based on actual knowledge.
- Output ONLY a JSON object: {{"label": "<label>"}}. No reasoning.

CLAIM: {claim}
"""


def call_judge(client: OpenAI, prompt: str, model: str, temperature: float,
               valid_labels: tuple, retries: int = 3) -> tuple[str, str]:
    """Returns (label, raw_response). Falls back to 'PARSE_ERROR' on failure."""
    for attempt in range(retries):
        try:
            resp = client.chat.completions.create(
                model=model,
                temperature=temperature,
                max_tokens=40,
                response_format={"type": "json_object"},
                messages=[{"role": "user", "content": prompt}],
            )
            raw = resp.choices[0].message.content
            try:
                obj = json.loads(raw)
                label = obj.get("label", "").strip()
                if label in valid_labels:
                    return label, raw
                for canon in valid_labels:
                    if label.lower() == canon.lower():
                        return canon, raw
                return "PARSE_ERROR", raw
            except json.JSONDecodeError:
                return "PARSE_ERROR", raw
        except Exception as e:
            if attempt < retries - 1:
                print(f"  retry {attempt+1} after error: {e}", flush=True)
                time.sleep(2 ** attempt)
            else:
                print(f"  WARNING: skip after {retries} failures: {e}",
                      flush=True)
                return "ERROR", str(e)


def derive_final_label(check1_label: str, check2_label: str) -> str:
    # Causal Maynez framing:
    #   intrinsic  = rewrite transforms info present in E_0 (source-anchored)
    #   extrinsic_* = rewrite generates info NOT in E_0 (autonomously generated);
    #                 sub-split by world-truth via Check 2
    if check1_label == "intrinsic":
        return "intrinsic"
    if check1_label == "extrinsic":
        if check2_label == "world_true":
            return "extrinsic_true"
        if check2_label == "world_false":
            return "extrinsic_false"
        if check2_label == "world_uncertain":
            return "extrinsic_uncertain"
        return f"extrinsic_{check2_label.lower()}"  # error/parse states
    return f"check1_{check1_label.lower()}"  # error/parse


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--details", type=Path, default=DEFAULT_DETAILS)
    parser.add_argument("--chains",  type=Path, default=DEFAULT_CHAINS,
                        help="Used to attach the E_0 source text per chain.")
    parser.add_argument("--instruction-type", default="elaborate")
    parser.add_argument("--out",     type=Path, default=DEFAULT_OUT)
    parser.add_argument("--model",   default="gpt-4o-mini")
    parser.add_argument("--temperature", type=float, default=0.0)
    parser.add_argument("--limit", type=int, default=None,
                        help="Max claims to classify (None = all).")
    args = parser.parse_args()

    api_key = os.environ.get("OPENAI_API_KEY")
    if not api_key:
        print("ERROR: set OPENAI_API_KEY before running.", file=sys.stderr)
        sys.exit(1)
    client = OpenAI(api_key=api_key)

    details = pd.read_csv(args.details)
    chains  = pd.read_csv(args.chains)

    # Attach E_0 (step=0) text to each row via (qid, group, instruction, run)
    e0_lookup = (
        chains[chains["step"] == 0]
        .set_index(["qid", "group", "instruction_type", "run"])["text"]
        .to_dict()
    )

    target = details[
        (details["instruction_type"] == args.instruction_type)
        & (details["label"] == "NOT_SUPPORTED")
    ].copy()
    print(f"NOT_SUPPORTED claims for {args.instruction_type}: {len(target)}")

    if args.limit:
        target = target.head(args.limit)
        print(f"Limited to first {args.limit}.")

    # Resume support
    done_keys: set = set()
    if args.out.exists():
        prev = pd.read_csv(args.out)
        done_keys = set(zip(prev["qid"], prev["instruction_type"],
                            prev["run"], prev["step"], prev["fact"]))
        print(f"Resume: {len(done_keys)} already done.")

    rows = []
    n_done = 0
    n_total = len(target)
    t_start = time.time()

    for _, r in target.iterrows():
        key = (r["qid"], r["instruction_type"], r["run"], r["step"],
               r["fact"])
        if key in done_keys:
            n_done += 1
            continue

        e0 = e0_lookup.get((r["qid"], r["group"], r["instruction_type"],
                            r["run"]))
        if e0 is None:
            print(f"  skip — missing E_0 for {key}", flush=True)
            n_done += 1
            continue

        # Check 1 (causal): intrinsic vs extrinsic — origin of the claim
        check1_label, check1_raw = call_judge(
            client,
            CHECK1_PROMPT.format(source=e0, claim=str(r["fact"])),
            args.model, args.temperature,
            ("intrinsic", "extrinsic"),
        )

        # Check 2: world truth — only if Check 1 was 'extrinsic'
        if check1_label == "extrinsic":
            check2_label, check2_raw = call_judge(
                client,
                CHECK2_PROMPT.format(claim=str(r["fact"])),
                args.model, args.temperature,
                ("world_true", "world_false", "world_uncertain"),
            )
        else:
            check2_label, check2_raw = "", ""

        final = derive_final_label(check1_label, check2_label)

        rows.append({
            "qid": r["qid"],
            "group": r.get("group", ""),
            "instruction_type": r["instruction_type"],
            "run": r["run"],
            "step": r["step"],
            "fact": r["fact"],
            "check1_label": check1_label,
            "check1_raw":   check1_raw,
            "check2_label": check2_label,
            "check2_raw":   check2_raw,
            "final_taxonomy": final,
        })
        n_done += 1

        if len(rows) >= 25:
            pd.DataFrame(rows).to_csv(
                args.out, mode="a",
                header=not args.out.exists(), index=False,
            )
            elapsed = time.time() - t_start
            eta = (n_total - n_done) * (elapsed / max(n_done, 1))
            print(f"  [{n_done}/{n_total}] ETA {eta/60:.1f} min",
                  flush=True)
            rows = []

    if rows:
        pd.DataFrame(rows).to_csv(
            args.out, mode="a",
            header=not args.out.exists(), index=False,
        )

    print(f"\nDone in {(time.time()-t_start)/60:.1f} min")
    print(f"Saved: {args.out}")

    # Summary
    out = pd.read_csv(args.out)
    print("\n=== Final taxonomy distribution ===")
    print(out["final_taxonomy"].value_counts().to_string())
    print("\n=== Per step ===")
    print(pd.crosstab(out["step"], out["final_taxonomy"]).to_string())
    print("\n=== Per step (proportion %) ===")
    print((pd.crosstab(out["step"], out["final_taxonomy"],
                       normalize="index") * 100).round(1).to_string())

    # Bound on extrinsic_false (lower = strict; upper = if uncertain were all false)
    e_false   = (out["final_taxonomy"] == "extrinsic_false").sum()
    e_unc     = (out["final_taxonomy"] == "extrinsic_uncertain").sum()
    e_true    = (out["final_taxonomy"] == "extrinsic_true").sum()
    intrinsic = (out["final_taxonomy"] == "intrinsic").sum()
    total     = len(out)
    print()
    print(f"=== Bounds on hallucination types (n={total}) ===")
    print(f"  intrinsic (contradicts E_0):       {intrinsic} ({100*intrinsic/total:.1f}%)")
    print(f"  extrinsic_true  (parametric leak): {e_true}  ({100*e_true/total:.1f}%)")
    print(f"  extrinsic_false (lower bound):     {e_false} ({100*e_false/total:.1f}%)")
    print(f"  extrinsic_false (upper bound):     {e_false + e_unc} "
          f"({100*(e_false+e_unc)/total:.1f}%)  [if all uncertain were false]")
    print(f"  extrinsic_uncertain:               {e_unc} ({100*e_unc/total:.1f}%)")


if __name__ == "__main__":
    main()
